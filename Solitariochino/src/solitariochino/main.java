    private void closewindows() {
        if (JOptionPane.showConfirmDialog(this, "Â¿Desea salir del juego?",
                "Aviso", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

            if (JOptionPane.showConfirmDialog(this, "Â¿Desea guardar los datos?", "Aviso",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    savegame.setData(this.startTime,
                            solitariochino.getAll()-solitariochino.getNumall(),
                            String.valueOf(jComboBoxlevel.getSelectedItem()),
                            this.getTimeStart());
                JOptionPane.showMessageDialog(this, "Se han guardado los datos.", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                System.exit(0);
            } else {
                JOptionPane.showMessageDialog(this, "Los datos se perderÃ¡n.", "AtenciÃ³n", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
        }

    }
