    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try{
            if(aux<=19){
                arraynumeros[aux]=Integer.valueOf(jTextFieldentrada.getText());
                jTextArea1.append(String.valueOf(arraynumeros[aux])+" ");
                aux++;
                jTextFieldentrada.setText("");
                if(aux==20){
                    jTextFieldentrada.setEnabled(false);
                    jButton2.setEnabled(true);
                    jTextFieldentrada.setText("");
                    aux=0;
                }
            }else{
                jTextFieldentrada.setEnabled(false);
                jButton2.setEnabled(true);
                jTextFieldentrada.setText("");
                aux=0;
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Introduce valores enteros"
                    + "No se admite caracteres."
                    + "Ni numeros decimales.","Aviso",  INFORMATION_MESSAGE);
            e.printStackTrace();
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

       for(int i=0; i<(arraynumeros.length/2); i++){
           aux=arraynumeros[(arraynumeros.length)-(i+1)];
           arraynumeros[(arraynumeros.length)-(i+1)]=arraynumeros[i];
           arraynumeros[i]=aux; 
       } 
       jTextArea1.append("\n");
       
       for(int i=0;i<arraynumeros.length;i++){
           jTextArea1.append(String.valueOf(arraynumeros[i])+" ");
       }
    }//GEN-LAST:event_jButton2ActionPerformed

