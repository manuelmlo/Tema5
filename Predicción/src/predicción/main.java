    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
            DocumentBuilder builder = factory.newDocumentBuilder();
            URL url= new URL(jTextFieldurl.getText());
            Document document=builder.parse(url.openStream());
            
            NodeList localidad=document.getElementsByTagName("nombre");
            Node namelocalidad=localidad.item(1);
            jTextArea1.append(namelocalidad.getNodeName());
            jTextArea1.append(namelocalidad.getNodeValue());
            Element root = document.getElementById("prediccion");
            NodeList tempmax = root.getElementsByTagName("temperatura");
            for(int i=0; i<tempmax.getLength();i++){
                Node itemtemp =tempmax.item(i);
                NodeList listitemtemp=itemtemp.getChildNodes();
                jTextArea1.append(itemtemp.getNodeName()+"\n");
                for(int j=0; i<listitemtemp.getLength();j++){
                    if(itemtemp.getNodeType()==Node.ELEMENT_NODE){
                        jTextArea1.append(listitemtemp.item(j).getNodeName()+" :\n");
                        if(listitemtemp.item(j).getNodeType()==Node.TEXT_NODE){
                            jTextArea1.append(listitemtemp.item(j).getTextContent());
                        }
                    }
                }
            } 
        }catch(MalformedURLException ex){
            
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

